public class Creating_and_using_variables {
    public static void main(String[]args){
        char var1='H';
        System.out.println(var1);
        String var2="Hello";
        System.out.println(var2);
        Boolean var3=true;
        System.out.println(var3);
        int var4=9;
        System.out.println(var4);
        double var5=1.23;
        System.out.println(var5);

    }
}